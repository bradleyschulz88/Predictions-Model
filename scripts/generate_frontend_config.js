#!/usr/bin/env node
/** Write dashboard/config.js from Vercel / local environment variables. */

import { writeFileSync } from "node:fs";
import { resolve, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const root = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const output = resolve(root, "dashboard", "config.js");

const config = {
  supabaseUrl: process.env.NEXT_PUBLIC_SUPABASE_URL || process.env.SUPABASE_URL || "",
  supabaseAnonKey: process.env.NEXT_PUBLIC_SUPABASE_ANON_KEY || process.env.SUPABASE_ANON_KEY || "",
  apiBase: process.env.NEXT_PUBLIC_API_BASE || "",
};

const contents = `// Generated by scripts/generate_frontend_config.js — do not edit by hand.
window.APP_CONFIG = ${JSON.stringify(config, null, 2)};
`;

writeFileSync(output, contents, "utf8");
console.log("Wrote dashboard/config.js");
